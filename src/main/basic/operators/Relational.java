package main.basic.operators;

/**
 * Class to work with relational operators
 * 
 * @author Roba
 *
 */
public class Relational {
    
    public static void main(String[] args) {
	int a = 10;
	int b = 20;
	System.out.println("a == b " + (a == b));
	System.out.println("a != b " + (a != b));
	System.out.println("a > b " + (a > b));
	System.out.println("a < b " + (a < b));
	System.out.println("b >= a " + (b >= a));
	System.out.println("b <= a " + (b <= a));
    }

}
